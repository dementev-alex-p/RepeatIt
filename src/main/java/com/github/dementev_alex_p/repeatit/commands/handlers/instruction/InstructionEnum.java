    InstructionEnum(String code, String buttonText, String text) {
        this.code = code;
        this.buttonText = buttonText;
        this.text = text;
        this.additionalInstructions = new ArrayList<>();
    }

    public static InstructionEnum findByCode(final String code) {
        return Stream.of(values())
                .filter(c -> c.getCode().equals(code))
                .findAny()
                .orElseThrow(() -> new RuntimeException("Unknown command: " + code));
    }

